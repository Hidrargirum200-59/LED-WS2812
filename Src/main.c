void string_process(char* buffer)
{
	char tmpstr[300] = "log: ";
	strcat(tmpstr, buffer);
	strcat(tmpstr, "\r\n");
	HAL_UART_Transmit(&huart2, (uint8_t*)tmpstr, strlen(tmpstr), 0x1000);
	ws2812_light_from_string(buffer);
}

void UART2_RxCpltCallback()
{
	uint8_t sym;
	sym = USB_IN[0];
	if (usb_sym_count > USB_BUF_SIZE - 1)
	{
		usb_sym_count = 0;
		HAL_UART_Receive_IT(&huart2, (uint8_t*)USB_IN, 1);
		return;
	}
	usb_buffer[usb_sym_count] = sym;
	//USB_IN[usb_sym_count] = sym;
	if (sym == 0x0A)
	{
		usb_buffer[usb_sym_count + 1] = 0;
		string_process((char*)usb_buffer);
		usb_sym_count = 0;
		HAL_UART_Receive_IT(&huart2, (uint8_t*)USB_IN, 1);
		return;
	}
	usb_sym_count++;
	HAL_UART_Receive_IT(&huart2, (uint8_t*)USB_IN, 1);
}
